/*
                     文件的读写模式：
   r：read 只读模式，不能写，强行写会报错

   w：write 只写模式，
        细节一：如果文件不存在，那么会先创建文件，但是要保证前面文件夹存在
        细节二：如果文件已存在，先把文件清空
        细节三：都是fopen底层函数干的

   a：append 追加写出模式
        细节一：如果文件不存在，那么会先创建文件，但是要保证前面文件夹存在
        细节二：如果文件已存在，不会把文件清空，而是接着写
        细节三：都是fopen底层函数干的

   rb：只读模式（二进制）
   wb：只写模式（二进制）
   ab：追加写出模式（二进制）


   r+：允许读和写
   w+：允许读和写
   a+：允许读和写
   rb+：只读模式（二进制）
   wb+：只写模式（二进制）
   ab+：追加写出模式（二进制）



*/